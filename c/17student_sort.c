// 题目内容：
// 程序读入n个学生的信息，每个学生包含学号、姓名、C语言成绩、高数成绩、英语成绩。先计算每个学生的总成绩，再按照总成绩降序排序；若总成绩相同，按照C语言成绩降序排序；若仍相同，按照学号升序排序。最终输出排序后的所有学生信息。
//
// 输入格式:
// 第一行是一个正整数n，表示学生人数，n<=50；
// 接下来n行，每行对应一个学生的信息，依次为学号（整数）、姓名（纯英文，无空格，长度<=10）、C语言成绩、高数成绩、英语成绩，各项之间用空格分隔，所有成绩均为0~100的整数。
//
// 输出格式：
// 共n行，每行对应一个学生的信息，依次为学号、姓名、总成绩、C语言成绩、高数成绩、英语成绩，各项之间用一个空格分隔，行末无多余空格。
//
// 输入样例：
// 3
// 2024003 zhang 85 90 88
// 2024001 li 92 88 90
// 2024002 zhao 92 88 90
//
// 输出样例：
// 2024001 li 270 92 88 90
// 2024002 zhao 270 92 88 90
// 2024003 zhang 263 85 90 88
