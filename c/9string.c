// 题目内容：
// 题目说起来很简单，你会读到两个字符串，每个字符串占据一行，每个字符串的长度均小于10000字符，而且第一个字符串的长度小于第二个字符串的。你的程序要找出第一个字符串在第二个字符串中出现的位置，输出这些位置，如果找不到，则输出-1。
// 注意，第一个字符的位置是0。
// 注意，第一个字符串在第二个字符串中的位置可能不止一处。
// 注意，字符串中可能含有空格。
// 注意，两个字符串的长度一定大于0。
//
// 输入格式:
// 两个字符串，一行一个。
//
// 输出格式：
// 第一个字符串在第二个字符串中出现的位置，按照从小到到的顺序排列，每个数字后面有一个空格。
// 如果在第二个字符串中找不到第一个字符串，则输出-1。
//
// 输入样例：
// abba
// ababbba abbabbabbabbaacc
//
// 输出样例：
// 8 11 14 17 

#include <stdio.h>  // 标准输入输出库，提供 fgets、printf 等函数
#include <string.h> // 字符串处理库，提供 strcspn、strlen 等函数

int main(void) {
    char str1[10001], str2[10001];  // 定义两个字符串数组，容量均为 10001
    
    // 库函数：fgets
    // 功能：从指定流（这里是标准输入 stdin）中读取一行字符串
    // 参数：存储位置、最大读取字符数、输入流
    fgets(str1, 10001, stdin);  // 读取第一个字符串
    fgets(str2, 10001, stdin);  // 读取第二个字符串
    
    // 库函数：strcspn
    // 功能：返回字符串中第一个属于指定字符集的位置
    // 这里用于找到换行符 '\n' 的位置
    str1[strcspn(str1, "\n")] = '\0';  // 去除 str1 末尾的换行符
    str2[strcspn(str2, "\n")] = '\0';  // 去除 str2 末尾的换行符
    
    // 库函数：strlen
    // 功能：返回字符串的长度（不包括末尾的 '\0'）
    int len1 = (int)strlen(str1);  // 获取 str1 的长度
    int len2 = (int)strlen(str2);  // 获取 str2 的长度
    
    int found = 0;  // 标记是否找到匹配
    
    // 外层循环：遍历 str2 中所有可能的起始位置
    for (int i = 0; i <= len2 - len1; i++) {
        int match = 1;  // 标记当前位置是否匹配
        
        // 内层循环：逐个字符比较
        for (int j = 0; j < len1; j++) {
            if (str2[i + j] != str1[j]) {  // 字符不匹配
                match = 0;  // 标记不匹配
                break;      // 跳出内层循环
            }
        }
        
        if (match) {  // 如果找到匹配
            // 库函数：printf
            // 功能：格式化输出到标准输出
            printf("%d ", i);  // 输出起始位置
            found = 1;  // 标记已找到
        }
    }
    
    if (!found) {  // 如果没有找到任何匹配
        printf("-1");  // 输出 -1
    }
    
    return 0;  // 程序正常结束
}
