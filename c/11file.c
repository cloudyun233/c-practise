// 题目2内容：
// 位运算练习：
// 请编写程序，读入一个正整数，分别输出它的二进制表示中1的个数、左移1位后的结果、右移1位后的结果。
// 提示：可以使用位运算符&、<<、>>来完成。
//
// 输入格式:
// 一个正整数。
//
// 输出格式：
// 三个整数，分别表示：1的个数、左移1位的结果、右移1位的结果，每个数字后面跟一个空格。
//
// 输入样例：
// 10
//
// 输出样例：
// 2 20 5 


// 主函数，程序入口
int main() {
    // num：存储用户输入的整数
    // count：统计二进制中1的个数，初始化为0
    // temp：临时存储num的值，避免修改原输入
    int num, count = 0, temp;
    
    // 从用户输入读取一个整数
    scanf("%d", &num);
    // 将输入的值保存到temp中，后续操作使用temp
    temp = num;
    
    // 循环：当temp大于0时，逐位检查二进制
    while (temp > 0) {
        // 使用位与运算(temp & 1)检查最低位是否为1
        // 如果结果为真（非0），说明最低位是1
        if (temp & 1) {
            // 统计1的个数加1
            count++;
        }
        // 将temp右移一位（相当于除以2），检查下一位
        temp >>= 1;
    }
    
    // 输出结果：
    // count：二进制中1的个数
    // num << 1：num左移一位（相当于乘以2）
    // num >> 1：num右移一位（相当于除以2，整数除法）
    printf("%d %d %d ", count, num << 1, num >> 1);
    
    // 程序正常结束，返回0
    return 0;
}
