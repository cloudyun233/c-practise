// 题目内容：
// 程序读入一段英文文本，文本以英文句号.结束。请统计文本中每个英文单词出现的次数，单词之间以空格、英文逗号,、英文句号.分隔，单词不区分大小写（如Hello和hello视为同一个单词），最终按照单词出现次数降序输出；若次数相同，按照单词字典序升序输出。
//
// 输入格式:
// 一行字符串，为输入的英文文本，长度不超过1000，以.结束，仅包含英文字母、空格、英文逗号、英文句号。
//
// 输出格式：
// 每行输出一个单词和其出现次数，格式为单词:次数，单词统一输出为小写字母。
//
// 输入样例：
// Hello, hello world! This is a test, hello world.
//
// 输出样例：
// hello:3
// world:2
// a:1
// is:1
// test:1
// this:1
